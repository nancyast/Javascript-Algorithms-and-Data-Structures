// TIME COMPLEXITY
// 1. Arithmetic operations are constant.
// 2. Varriable assignment is contant.
// 3. Accessing elements in an array(by index) or object(by key) is constant.
// 4. In a loop, the complexity is the length of the loop times the complexity of whatever happens inside of the loop.

// SPACE COMPLEXITY
// Auxiliary space complexity - space required by the algorithm, not including space taken up by the inputs.
// 1. Most primitives (boolean, numbers, undefined, null) ara constant space. O(1).
// 2. Strings require O(n) space (where n is the string length).
// 3. Reference types are generally O(n), where n is the length(for array) or the number of keys(for objects).
